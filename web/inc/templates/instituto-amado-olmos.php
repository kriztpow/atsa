			   		<h3>
				  		<span class="text-title-accordion">
					  		Historia
					  	</span>
					  <span class="icon-suma"></span>
					</h3>
				  	<div class="contenido-accordion-cursos">
				  		<p>
							Hace 27 años nació la Escuela de Enfermería del Sindicato de Trabajadores de la Sanidad, filial Bs. As, con la misión de brindar una formación a todos aquellos que quieran desarrollarse en la actividad. Durante estos 27 años hemos logramos un alto nivel de contenidos y metodologías de capacitación, así como una gran cantidad de inscriptos por año. 
						</p>

						<p>
							Mucho ha pasado desde el 2 de Abril de 1938 que, bajo la conducción del gremio, los directivos y el cuerpo docente se inauguraba la Escuela de Enfermeros y Enfermeras La Cruz del Sacrificio. Por diferentes acontecimientos históricos en el año 1968 la escuela 
							cerró y luego reinició sus actividades en 1990 cambiando su nombre por el actual: Instituto Amado Olmos. 
						</p>

						<p>
							La adecuación de sus Programas y de los requisitos a las normativas vigentes le permitió obtener el reconocimiento como Instituto oficial. A partir de entonces, fueron egresando de la Escuela auxiliares de enfermería y enfermeros profesionales del ámbito público, privado y fundamentalmente, de obras sociales, lo cual amplió y diversificó el conjunto de los mismos. Siempre manteniendo la premisa de transformar y/o reemplazar al personal mayoritariamente empírico.Así siguió desarrollándose y creciendo nuestro Instituto 
							Amado Olmos, que nos enorgullece como trabajadores de la Sanidad y como integrantes de una comunidad.
						</p>
				  	</div>
			   	<!-- item tab -->
			   		<h3>
				  		<span class="text-title-accordion">
					  		Misión
					  	</span>
					  <span class="icon-suma"></span>
					</h3>
				  	<div class="contenido-accordion-cursos">
				  		<p>
				  			El Instituto Amado Olmos tiene como objetivo la formación de futuros enfermeros. A través de la Carrera de Técnico Superior en Enfermería, todos los que tengan interés en esta profesión pueden capacitarse con los que más saben de salud, los trabajadores de la Sanidad. 
				  		</p>
				  	</div>
			   	
				<!-- item tab -->
			   		<h3>
				  		<span class="text-title-accordion">
					  		Prácticas profesionales
					  	</span>
					  <span class="icon-suma"></span>
					</h3>
				  	<div class="contenido-accordion-cursos">
				  		<p>
							Las prácticas profesionales tienen dos momentos: uno está referido al desarrollo en el gabinete y el otro son las prácticas en campo, que se realizan en diferentes hospitales, sanatorios, clínicas y en comunidad.
						</p>
						<p>
							El gabinete se realiza en un espacio simulado para que el estudiante pueda articular la teoría con la práctica. Estas prácticas simuladas, organizadas y diseñadas en forma de rotatorio permiten a los estudiantes ir aplicando los diferentes conocimientos que van adquiriendo durante la formación. 
						</p>
						<p>
							Estas prácticas están atravesadas por un Modelo de Atención de Enfermería que la Escuela adhiere. Tiene presente que para poder actuar con criterio profesional es necesario una fuerte argumentación conceptual, que guiará la identidad de nuestros egresados y de la enfermería en general: el ser, el saber y el hacer. 
							Nuestro Gabinete cuenta con recursos tanto tecnológicos como materiales que permiten la adquisición de competencias propias de la ciencia. 
						</p>
				  	</div>

