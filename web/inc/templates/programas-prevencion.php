                    </p>

                    <p class="pp-parrafo-prevencion">
                        Es el principal Factor de Riesgo para padecer Enfermedades Cardiovasculares como infarto Agudo de Miocardio y Accidentes cerebrovasculares. Produce daños en los riñones, retina y sistema nervioso. 
                    </p>

                    <h4 class="pp-titulo-prevencion">Síntomas | Diagnóstico</h4>

                    <p class="pp-parrafo-prevencion">
