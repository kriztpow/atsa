                    </p>
                    <p class="pp-parrafo-prevencion">
                        Si usted no ha efectuado controles con el Médico Clínico en el último año, debe realizarlo sin demoras. Su médico le informará los Valores de su Presión Arterial y si son elevados le indicará el camino a seguir.
                    </p>

                    <p class="pp-parrafo-prevencion">
                        <strong>
                            Los pacientes hipertensos que cumplen su tratamiento tienen menos probabilidades de desarrrollar hipertensión grave.
