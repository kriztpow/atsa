                <h3>
                    <span class="text-title-accordion">
                        Prevención y Detección Precoz de la Diabetes
                    </span>
                    <span class="icon-cross"></span>
                </h3>
                <div class="programa-prevencion">
                    
                    <p class="pp-parrafo-prevencion">
                        La Diabetes es una enfermedad crónica producida por un aumento permanente del azúcar (glucosa) en la sangre denominada hiperglucemia. Cuando esto se produce el organismo envía una señal al páncreas para que secrete insulina. La insulina permite el ingreso de la glucosa a las células y es utilizada como combustible para vivir y desarrollarse. Si este proceso se altera la glucosa no entra a las células y permanece en la sangre produciendo Hiperglucemia, ya sea por falta de insulina o por menor acción de la misma.
                    </p>

                    <h4 class="pp-titulo-prevencion">Síntomas | Diagnóstico</h4>

                    <h5 class="pp-subtitulo-prevencion">
                        ¿Qué provoca la Hiperglucemia a corto y a largo plazo?
                    </h5>
                    <ul>
                        <li class="pp-lista-prevencion">
                            Aumento de la sed, el apetito y la cantidad de orina
                        </li>
                        <li class="pp-lista-prevencion">
                            Debilidad y cansancio
                        </li>
                        <li class="pp-lista-prevencion">
                            Infecciones frecuentes de la piel y de los aparatos urinario y genital
                        </li>
                        <li class="pp-lista-prevencion">
                            Alteraciones de la visión
                        </li>
                        <li class="pp-lista-prevencion">
                            Dificultad para la cicatrización de heridas
                        </li>
                        <li class="pp-lista-prevencion">
                            Problemas cardíacos
                        </li>
                        <li class="pp-lista-prevencion">
                            Alteración de la función de los riñones
                        </li>
                        <li class="pp-lista-prevencion">
                            Lesiones en los pies
                        </li>
                    </ul>

                    <p class="pp-parrafo-prevencion">
                        <strong>
                            Su médico le indicará cuál es la alimentación más apropiada, la actividad física más conveniente y el medicamento más adecuado.
                        </strong>
                    </p>

                    <h4 class="pp-titulo-prevencion">Prevención | Tratamiento</h4>
                    <p class="pp-parrafo-prevencion">
                        La disminución de peso y el incremento de la actividad física regular disminuyen la aparición de la Diabetes.
                    </p>
                    <p class="pp-parrafo-prevencion">
                        El control es fundamental para evitar o retardar las complicaciones asociadas con la Hiperglucemia.
                    </p>
                    <p class="pp-parrafo-prevencion">
                        La alimentación debe incluir hidratos de carbono, proteínas, grasas, vitaminas y minerales. Este tipo de dietas permiten mantener un buen estado nutricional.
                    </p>
                    <p class="pp-parrafo-prevencion">
                        La actividad física regular disminuye la glucemia porque aumenta su consumo. Además ayuda a la reducción de peso.
                    </p>
                    <p class="pp-parrafo-prevencion">
                        Es fundamental un tratamiento farmacológico adecuado, no se automedique. Consulte a su médico.
                    </p>
                    <h4 class="pp-titulo-prevencion">
                        Recuerde:
                    </h4>
                    <p class="pp-parrafo-prevencion">
                        Realizar controles periódico de salud con determinación de glucosa en sangre para diagnosticar la dibetes a tiempo.
                    </p>
                </div>


